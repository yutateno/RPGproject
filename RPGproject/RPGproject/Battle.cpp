#include "Manager.h"

Battle::Battle() {
	this->endFlag = false;
	this->nextScene = eScene::S_End;
	this->step = eStep::Start;
	this->startCount = 0;
	this->endCount = 0;

	// 画像
	Gr_Back = LoadGraph("img\\battle_background.png");		// 背景

	// コマンド状態
	command = NEUTRAL;
	preCommand = command;

	damageFlag = false;
	damageWidth = 0;

	textFlag = true;

	// カーソル座標
	cursorX = 0;
	cursorY = 0;
}
Battle::~Battle() {
	DeleteGraph(Gr_Back);
}

void Battle::UpDate() {
	switch (this->step) {
	case eStep::Start:	// 開始画面
		this->UpDate_Start();
		break;
	case eStep::Main:	// メイン処理画面
		this->UpDate_Main();
		break;
	case eStep::End:	// 終了画面
		this->UpDate_End();
		break;
	default:
		this->endFlag = true;	// エラー終了
		break;
	}
}
void Battle::UpDate_Start() {
	this->startCount++;

	if (this->startCount < 50) return;	// 50フレームで開始画面終了
	this->step = eStep::Main;
}
void Battle::UpDate_Main() {
	// 直前のコマンドを保存
	preCommand = command;

	// カーソル移動
	if (KeyData::Get(KEY_INPUT_UP) == 1 && cursorY>0)
	{
		cursorY--;
	}
	if (KeyData::Get(KEY_INPUT_DOWN) == 1 && cursorY<2)
	{
		cursorY++;
	}

	// Zキーで決定
	if (KeyData::Get(KEY_INPUT_Z) == 1) {
		switch (command)
		{
		case NEUTRAL:	// 初期
			if (cursorY == 0)		// 攻撃選択時
			{
				// ここに攻撃力などを代入する式を追加する
				damageWidth = 0;		// 振れ幅
				damageFlag = true;		// Managerに、enemyにダメージを与えるよう指示する
				command = NEUTRAL;		// commandをNEUTRALに戻す
				command = ATTACK;
			}
			else if (cursorY == 1)// 魔法選択時
			{
				// ここに攻撃力などを代入する式を追加する
				damageWidth = 3;		// 振れ幅
				damageFlag = true;		// Managerに、enemyにダメージを与えるよう指示する
				command = NEUTRAL;		// commandをNEUTRALに戻す
				command = MAGIC;
			}
			else{					// 逃げる選択時
				command = RUN_AWAY;
			}
			break;

		case ATTACK:	// 攻撃メニュー
			break;

		case MAGIC:		// 魔法メニュー
			break;

		case RUN_AWAY:	// 逃げる
			break;

		default:		// 在り得ない。エラー
			break;
		}
	}

	// コマンドの変化からカーソルを補正
	if (preCommand != command)
	{
		cursorY = 0;		// カーソルをもとの位置に戻す
	}

	// 逃げたら
	if (command == RUN_AWAY)
	{
		step = eStep::End;	// 戦闘終了
	}
}
void Battle::UpDate_End() {
	this->endCount++;

	if (this->endCount < 50) return;	// 50フレームで終了画面終了
	this->endFlag = true;
}
void Battle::Draw() {
	switch (this->step) {
	case eStep::Start:	// 開始画面
		this->Draw_Start();
		break;
	case eStep::Main:	// メイン処理画面
		this->Draw_Main();
		break;
	case eStep::End:	// 終了画面
		this->Draw_End();
		break;
	default:
		this->endFlag = true;	// エラー終了
		break;
	}
}
void Battle::Draw(bool flag) {
	// 進行状態を受け取り表示するかどうかを決める
	textFlag = flag;

	switch (this->step) {
	case eStep::Start:	// 開始画面
		this->Draw_Start();
		break;
	case eStep::Main:	// メイン処理画面
		this->Draw_Main();
		break;
	case eStep::End:	// 終了画面
		this->Draw_End();
		break;
	default:
		this->endFlag = true;	// エラー終了
		break;
	}
}
void Battle::Draw_Start() {
	DrawStringToHandle(0, 0, "戦闘画面", WHITE, Font::Get(eFont::SELECT));
	DrawFormatStringToHandle(0, 100, WHITE, Font::Get(eFont::SELECT), "開始画面%d", this->startCount);
}
void Battle::Draw_Main() {
	// 背景
	DrawGraph(0, 0, Gr_Back, true);

	if (textFlag)
	{
		// コマンド状態に応じて表示を変化
		switch (command)
		{
		case NEUTRAL:	// 初期
			DrawFormatString(0, 384, WHITE, "  攻撃");
			DrawFormatString(0, 416, WHITE, "  魔法");
			DrawFormatString(0, 448, WHITE, "  逃げる");
			break;

		case ATTACK:	// 攻撃メニュー
			break;

		case MAGIC:		// 魔法メニュー
			break;

		case RUN_AWAY:	// 逃げる
			break;

		default:		// 在り得ない。エラー
			break;
		}

		// カーソル
		//DrawStringToHandle(cursorX, 400 + (cursorY * 32), "▲", WHITE, Font::Get(eFont::SELECT));
		DrawFormatString(cursorX, 384 + (cursorY * 32), WHITE, "▲");
	}
}
void Battle::Draw_End() {
	DrawStringToHandle(0, 0, "戦闘画面", WHITE, Font::Get(eFont::SELECT));
	DrawFormatStringToHandle(0, 100, WHITE, Font::Get(eFont::SELECT), "終了画面%d", this->endCount);
}

